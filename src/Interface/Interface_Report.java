    public final void set_start_end(){
        Calendar xx = Calendar.getInstance();
        xx.setTime(new Date());
        xx.set(Calendar.DAY_OF_MONTH, 1);
        start_date = new Date();
        start_date = xx.getTime();
        xx.add(Calendar.MONTH, 1);
        xx.set(Calendar.DAY_OF_MONTH, 1);
        xx.add(Calendar.DATE, -1);
        end_date = new Date();
        end_date = xx.getTime();
    }
