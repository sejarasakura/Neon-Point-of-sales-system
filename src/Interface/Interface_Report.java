    private void summary_reportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_summary_reportActionPerformed
        // TODO add your handling code here:
        this.jScrollPane1.setViewportView(this.report_panel);
        set_start_end();
        Report_SummaryReport run = new Report_SummaryReport(this.start_date, this.end_date);
        run.generateSummaryReport();
        run.write_full_report();
        this.html_other_label.setText(run.getReport());
        this.graph_destination.removeAll();
        this.graph_destination.add(run.write_chart());
        this.graph_destination.updateUI();
    }//GEN-LAST:event_summary_reportActionPerformed

    private StyleSheet report_css(){
        StyleSheet css = new StyleSheet();
        try {
            BufferedReader br = new BufferedReader(new InputStreamReader(new FileInputStream("resource/html/css/report.css")));
            css.loadRules(br, null);
            br.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Interface_Report.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Interface_Report.class.getName()).log(Level.SEVERE, null, ex);
        }
        return css;
    }
    
