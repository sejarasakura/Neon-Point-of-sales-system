    private void bDelete_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDelete_btnActionPerformed
        
        Staff temp;
        if(staffTable.getSelectedRow() >= 0)
            temp = new Staff(model.getValueAt(staffTable.getSelectedRow(), 0).toString());
        else
            temp = new Staff(this.input_staff_id.getText());
        
        if(this.loginedStaff.getLevel().position() > temp.getLevel().position()){ 
            if(staffTable.getSelectedRow() >= 0){
                if (JOptionPane.showConfirmDialog(null, "Do you sure want to delete the following staff " + temp.getUser_id() , "Confirm delete", 0) == 0) {
                    model.removeRow(staffTable.getSelectedRow());
                    if (temp.delete_this()) {
                        System.out.println("The staff is successful deleted by user : " + SubInterface_Login_Staff.decode_id(this.getDataDutyStaff()));
                    } else {
                        System.out.println("The file is cannot access by the system, please check back the data");
                    }
                    this.staffTable.clearSelection();
                }
            }else{
                boolean found = false;
                for(int i = 0;i < staffTable.getRowCount(); i++){
                    if(model.getValueAt(i,0).equals(temp.getUser_id())){
                        if (JOptionPane.showConfirmDialog(null, "Do you sure want to delete the following staff " + temp.getUser_id(), "Confrim delete", 0) == 0) {
                            found = true;
                            staffTable.remove(i);
                            temp.delete_this();
                            set_input();
                        }
                    }
                }
            }
        }else{
            JOptionPane.showConfirmDialog(null, "Your position is lower than the selected staff, you unable to delete the employee higher position than you!!", "Delete Error", JOptionPane.WARNING_MESSAGE);
        }
        this.total_staff.setText("Total Staff Found : " + this.staffTable.getRowCount());
    }//GEN-LAST:event_bDelete_btnActionPerformed

