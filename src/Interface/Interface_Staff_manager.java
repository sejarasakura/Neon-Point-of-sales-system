    private void openInput(Staff selected_staff){
        if(this.loginedStaff.getLevel().level_number() > selected_staff.getLevel().position() || this.loginedStaff.getLevel().position() == 4){
            this.input_Staff_position.setEditable(true);
            this.input_Staff_position.setEnabled(true);
            this.input_joindate.setEnabled(true);
            this.input_address.setEditable(true);
            this.input_fName.setEditable(true);
            this.input_female_gender.setEnabled(true);
            this.input_male_gender.setEnabled(true);
            this.input_other_gender.setEnabled(true);
            this.input_lName.setEditable(true);
            this.input_phone.setEditable(true);
            this.input_password.setEditable(true);
            this.input_new_password.setEditable(true);
            this.input_staff_id.setEditable(false);
            this.input_staff_ic.setEditable(true);
            this.input_working_hours.setEditable(true);
            this.input_salary.setEditable(true);
            this.input_salary.setVerifyInputWhenFocusTarget(true);
            this.input_salary_rate.setEditable(true);
            this.input_email.setEditable(true);
            this.input_department.setEditable(true);
        this.input_salary_rate.setVerifyInputWhenFocusTarget(true);
        }
        else if (selected_staff.getUser_id() == null ? loginedStaff.getUser_id() == null : selected_staff.getUser_id().equals(loginedStaff.getUser_id())){
            this.input_address.setEditable(true);
            this.input_phone.setEditable(true);
            this.input_password.setEditable(true);
            this.input_new_password.setEditable(true);
            this.input_email.setEditable(true);
            this.input_salary.setVerifyInputWhenFocusTarget(true);
            this.input_salary_rate.setVerifyInputWhenFocusTarget(true);
        }
        else{
            JOptionPane.showConfirmDialog(null, "Your position is lower than the selected staff, you unable to edit all the employee higher position than you!!", "", JOptionPane.DEFAULT_OPTION);
        }
    }
    
