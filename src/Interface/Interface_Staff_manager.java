    private void set_input(String staff_id) {
        Staff staff = new Staff(staff_id);
        this.input_Staff_position.setSelectedIndex(staff.getLevel().level_number());
        this.input_address.setText(staff.getAddress());
        this.input_fName.setText(staff.getfName());
        this.input_joindate.setText("Date : " + WriteSql.DATE_FORMAT.format(staff.getJoin_date()));
        this.input_lName.setText(staff.getlName());
        this.input_password.setText("");
        this.input_phone.setText(staff.getPhoneNumber());
        this.input_new_password.setText("");
        this.input_staff_id.setText(staff.getUser_id());
        this.input_staff_ic.setText(staff.getUser_ic());
        this.input_department.setSelectedIndex(staff.getDepartment().toIndex());
        this.input_email.setText(staff.getEmail());
        this.input_salary.setText(String.format("%.2f", staff.getSalary()));
        this.input_salary_rate.setText(String.format("%.2f", staff.getSalary_rate_hours()));
        this.input_working_hours.setText(staff.getWorkingHours() + "");
        this.input_female_gender.setSelected(false);
        this.input_male_gender.setSelected(false);
        this.input_other_gender.setSelected(false);
        if(null == staff.getGender()){
            this.input_other_gender.setSelected(true);
        }
        else switch (staff.getGender()) {
            case FEMALE:
                this.input_female_gender.setSelected(true);
                break;
            case MALE:
                this.input_male_gender.setSelected(true);
                break;
            default:
                this.input_other_gender.setSelected(true);
                break;
        }
        System.out.println(staff.toString());
    }
